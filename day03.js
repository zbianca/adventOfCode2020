function finder(input, right, down) {
  let count = 0;
  let x = 0
  let y = 0;
  let list = input.trim().split("\n");

  while (y < list.length - down) {
    y += down;
    x = (x + right) % list[0].length;
    let found = list[y].charAt(x);
    if (found === '#') count++;
  }

  return count;
}

// Part two:
// Right 1, down 1.
// Right 3, down 1. (This is the slope you already checked.)
// Right 5, down 1.
// Right 7, down 1.
// Right 1, down 2.

function multTrees() {
  return finder(input, 1, 1) * finder(input, 3, 1) * finder(input, 5, 1) * finder(input, 7, 1) * finder(input, 1, 2);
}

